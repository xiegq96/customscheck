//重写alert
window.alert = function(msg, callback){
	parent.layer.alert(msg, function(index){
		parent.layer.close(index);
		if(typeof(callback) === "function"){
			callback("ok");
		}
	});
}

//重写confirm式样框
window.confirm = function(msg, callback){
	parent.layer.confirm(msg, {btn: ['确定','取消']},
	function(){//确定事件
		if(typeof(callback) === "function"){
			callback("ok");
		}
	});
}

//选择一条记录
function getSelectedRow() {
    var grid = $("#jqGrid");
    var rowKey = grid.getGridParam("selrow");
    if(!rowKey){
    	alert("请选择一条记录");
    	return ;
    }
    
    var selectedIDs = grid.getGridParam("selarrrow");
    if(selectedIDs.length > 1){
    	alert("只能选择一条记录");
    	return ;
    }
    return selectedIDs[0];
}

//选择多条记录
function getSelectedRows() {
    var grid = $("#jqGrid");
    var rowKey = grid.getGridParam("selrow");
    if(!rowKey){
    	alert("请选择一条记录");
    	return ;
    }
    
    return grid.getGridParam("selarrrow");
}

//判断是否为空
function isBlank(value) {
    return !value || !/\S/.test(value)
}

//将字符串‘yyyymmddhhmmss’或‘yyyymmdd’转化为‘yyyy-mm-dd hh:mm:ss’或‘yyyy-mm-dd’格式的日期
function stringToDate(str) {
    if(str!=null){
        if(str.length>=14){
            var year=str.slice(0,4);
            var month=str.slice(4,6);
            var date=str.slice(6,8);
            var hour=str.slice(8,10);
            var minute=str.slice(10,12);
            var second=str.slice(12,14);
            return year+'-'+month+'-'+date+' '+hour+':'+minute+':'+second;
        }else if(str.length>=8 && str.length<14){
            var year=str.slice(0,4);
            var month=str.slice(4,6);
            var date=str.slice(6,8);
            return year+'-'+month+'-'+date;
        }
    }
    return "";
}